// Operators
//  + - * / % ++ -- **

// 1. Addition
let a = 4;
let b = 5;
let c = a + b;

console.log("Addition: ", c)

// 2. Subtraction
let d = 10
let e = 5
let f = d - e

console.log("Subtraction: ", f)

// 3. Multiplication
let g = 4;
let h = 5;
let i = g * h;

console.log("Multiplication: ", i)

// 4. Division
let j = 10
let k = 3
let l = j / k

console.log("Division: ", l)

// 5. Modulus
let m = 10
let n = 3
let o = m % n

console.log("Modulus: ", o)

// 6. Increment
let p = 10
p++
console.log("Increment: ", p)

// 7. Decrement
let q = 10
q--
console.log("Decrement: ", q)

// 8. Exponentiation
let r = 2
let s = 4
let t = r ** s // 2^3
console.log("Exponentiation: ", t)